void check_pw(char * pass)
{
  int i = 0;
  for (i=0;i<4;i++) { 
       if (!(((pass[i] >= 'a') && (pass [i] <= 'z'))
             || ((pass[i] >= 'A') && (pass [i] <= 'Z'))
             || ((pass[i] >= '0') && (pass [i] <= '9')))) {
                   printf("Password not as per specifications\n");
                   exit(0);
       };
  };
};

void reverse(int hash)
{
    char *hash_ptr = (char *)&hash;
    for(int i = 0; i < 4; i++) {
        char c = (unsigned char)hash_ptr[i] % 128;
        if (c <= 9) {
            pass[i] = c + '0';
        } else if (c <= 35) {
            pass[i] = c - 10 + 'A';
        } else if (c <= 47) {
            pass[i] = c - 36 + 'a';
        } else if (c <= '9') {
            pass[i] = c;
        } else if (c <= 64) {
            pass[i] = c - 58 + 'm';
        } else if (c <= 'Z') {
            pass[i] = c;
        } else if (c <= 96) {
            pass[i] = c - 91 + 't';
        } else if (c <= 'z') {
            pass[i] = c;
        } else if (c == 123) {
            pass[i] = 'z';
        } else if (c == 124) {
            pass[i] = '0';
        } else if (c == 125) {
            pass[i] = '1';
        } else if (c == 126) {
            pass[i] = '2';
        } else if (c == 127) {
            pass[i] = '3';
        }
    }
}

void reverse_seq(int hash, int iter) {
    reverse(hash ^ iter);
}

